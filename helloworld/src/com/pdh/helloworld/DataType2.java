package com.pdh.helloworld;

public class DataType2 {
	
	public static void main(String[] args) {
		char ch1 = 'a';
		char ch2 = 'A';
		char ch3 = '1';
		
		System.out.println(ch1);
		System.out.println(ch2);
		System.out.println(ch3);
		System.out.println("--------------");
		System.out.print(ch1 + "\n");
		System.out.print(ch2 + "\n");
		System.out.print(ch3 + "\n");
		System.out.println("--------------");
		System.out.printf("121212  %c 232323\n", ch1);
		System.out.printf("가 %c 나다라  %d 마 %d바사\n", ch2, 135, 232);
		System.out.println("--------------");
		System.out.printf("%c : %d \n", ch1, (int)ch1);
		System.out.printf("%c : %d \n", ch2, (int)ch2);
		System.out.printf("%c : %d \n", 66, 66);
		
		
		
		
		
		
		
		
		
		
		
	}
}
