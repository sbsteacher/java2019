package com.pdh.helloworld.method;

public class Exam2 {
	public static void main(String[] args) {
		squre(5);
		squre(50);
	}
	
	public static void squre(int num) {
		for(int z=0; z<num; z++) {
			for(int i=0; i<num; i++) {
				System.out.print("*");
			}
			System.out.println();
		}
		System.out.println();
		
	}
}
